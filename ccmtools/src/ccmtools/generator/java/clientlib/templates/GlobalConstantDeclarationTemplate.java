package ccmtools.generator.java.clientlib.templates;

import ccmtools.generator.java.clientlib.metamodel.*;

public class GlobalConstantDeclarationTemplate
{
  protected static String nl;
  public static synchronized GlobalConstantDeclarationTemplate create(String lineSeparator)
  {
    nl = lineSeparator;
    GlobalConstantDeclarationTemplate result = new GlobalConstantDeclarationTemplate();
    nl = null;
    return result;
  }

  protected final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
  protected final String TEXT_1 = "/*" + NL + " * This file was automatically generated by ";
  protected final String TEXT_2 = NL + " * <http://ccmtools.sourceforge.net>" + NL + " * ";
  protected final String TEXT_3 = NL + " * DO NOT EDIT!" + NL + " */" + NL + "" + NL + "package ";
  protected final String TEXT_4 = ";" + NL + "" + NL + "public interface ";
  protected final String TEXT_5 = NL + "{" + NL + "    final public static ";
  protected final String TEXT_6 = " value = (";
  protected final String TEXT_7 = ") ";
  protected final String TEXT_8 = ";" + NL + "}";
  protected final String TEXT_9 = NL;

  public String generate(Object argument)
  {
    StringBuffer stringBuffer = new StringBuffer();
     ConstantDef constant = (ConstantDef) argument;  
    stringBuffer.append(TEXT_1);
    stringBuffer.append( constant.generateCcmtoolsVersion());
    stringBuffer.append(TEXT_2);
    stringBuffer.append(constant.generateTimestamp());
    stringBuffer.append(TEXT_3);
    stringBuffer.append( constant.getJavaNamespace() );
    stringBuffer.append(TEXT_4);
    stringBuffer.append(constant.getIdentifier());
    stringBuffer.append(TEXT_5);
    stringBuffer.append(constant.getType().generateJavaMapping());
    stringBuffer.append(TEXT_6);
    stringBuffer.append(constant.getType().generateJavaMapping());
    stringBuffer.append(TEXT_7);
    stringBuffer.append(constant.generateConstantValue());
    stringBuffer.append(TEXT_8);
    stringBuffer.append(TEXT_9);
    return stringBuffer.toString();
  }
}
