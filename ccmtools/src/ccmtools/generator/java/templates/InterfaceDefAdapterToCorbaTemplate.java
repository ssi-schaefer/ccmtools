  {
    nl = lineSeparator;
    InterfaceDefAdapterToCorbaTemplate result = new InterfaceDefAdapterToCorbaTemplate();
    nl = null;
    return result;
  }

  protected final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
