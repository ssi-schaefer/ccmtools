package ccmtools.generator.java.templates;

import ccmtools.generator.java.metamodel.*;

public class ConstantDefInterfaceGlobalTemplate
{
  protected static String nl;
  public static synchronized ConstantDefInterfaceGlobalTemplate create(String lineSeparator)
  {
    nl = lineSeparator;
    ConstantDefInterfaceGlobalTemplate result = new ConstantDefInterfaceGlobalTemplate();
    nl = null;
    return result;
  }

  protected final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
  protected final String TEXT_1 = "/*" + NL + " * This file was automatically generated by ";
  protected final String TEXT_2 = NL + " * <http://ccmtools.sourceforge.net>" + NL + " * DO NOT EDIT!" + NL + " */" + NL + "" + NL + "package ";
  protected final String TEXT_3 = ";" + NL + "" + NL + "public interface ";
  protected final String TEXT_4 = NL + "{" + NL + "    final public static ";
  protected final String TEXT_5 = " value = (";
  protected final String TEXT_6 = ") ";
  protected final String TEXT_7 = ";" + NL + "}";
  protected final String TEXT_8 = NL;

  public String generate(Object argument)
  {
    final StringBuffer stringBuffer = new StringBuffer();
     ConstantDef constant = (ConstantDef) argument;  
    stringBuffer.append(TEXT_1);
    stringBuffer.append( constant.generateCcmtoolsVersion());
    stringBuffer.append(TEXT_2);
    stringBuffer.append( constant.generateJavaNamespace() );
    stringBuffer.append(TEXT_3);
    stringBuffer.append(constant.getIdentifier());
    stringBuffer.append(TEXT_4);
    stringBuffer.append(constant.getType().generateJavaMapping());
    stringBuffer.append(TEXT_5);
    stringBuffer.append(constant.getType().generateJavaMapping());
    stringBuffer.append(TEXT_6);
    stringBuffer.append(constant.generateJavaValue());
    stringBuffer.append(TEXT_7);
    stringBuffer.append(TEXT_8);
    return stringBuffer.toString();
  }
}
