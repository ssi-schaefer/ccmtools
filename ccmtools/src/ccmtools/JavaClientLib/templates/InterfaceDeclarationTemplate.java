package ccmtools.JavaClientLib.templates;

import java.util.Iterator;
import ccmtools.JavaClientLib.metamodel.*;

public class InterfaceDeclarationTemplate
{
  protected static String nl;
  public static synchronized InterfaceDeclarationTemplate create(String lineSeparator)
  {
    nl = lineSeparator;
    InterfaceDeclarationTemplate result = new InterfaceDeclarationTemplate();
    nl = null;
    return result;
  }

  protected final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
  protected final String TEXT_1 = "/*" + NL + " * This file was automatically generated by ..." + NL + " * <http://ccmtools.sourceforge.net>" + NL + " * DO NOT EDIT!" + NL + " */" + NL + "" + NL + "package ";
  protected final String TEXT_2 = ";" + NL + "                 " + NL + "import ccm.local.Components.*;" + NL + " " + NL + "/** " + NL + " * Java interface declaration (based on the corresponding IDL interface)." + NL + " */" + NL + "public interface ";
  protected final String TEXT_3 = " " + NL + "{";
  protected final String TEXT_4 = NL;
  protected final String TEXT_5 = NL;
  protected final String TEXT_6 = NL;
  protected final String TEXT_7 = NL + "}";

  public String generate(Object argument)
  {
    StringBuffer stringBuffer = new StringBuffer();
     InterfaceDefinition iface = (InterfaceDefinition) argument; 
    stringBuffer.append(TEXT_1);
    stringBuffer.append( iface.generateJavaNamespace() );
    stringBuffer.append(TEXT_2);
    stringBuffer.append( iface.getIdentifier() );
    stringBuffer.append(TEXT_3);
    stringBuffer.append(TEXT_4);
    stringBuffer.append(iface.generateConstantDeclarationList());
    stringBuffer.append(TEXT_5);
    stringBuffer.append(iface.generateAttributeDeclarationList());
    stringBuffer.append(TEXT_6);
    stringBuffer.append(iface.generateOperationDeclarationList());
    stringBuffer.append(TEXT_7);
    return stringBuffer.toString();
  }
}
